static uint16_t blink_timer;
static uint16_t after_blinked_timer;
static bool blinked = false;

void init_materia(bool master) {
  if (master) {
    display.shell = font_shell_master;
  } else {
    display.shell = font_shell_slave;
  }
  blink_timer = timer_read();
}

void update_master_status(void) {
  if (blinked) {
    if ((timer_elapsed(after_blinked_timer) >= 100)) {
      blink_timer = timer_read();
      blinked = false;
    }
  } else {
    if (timer_elapsed(blink_timer) >= 500) {
      if (rand() % 4 == 0) {
        after_blinked_timer = timer_read();
        blinked = true;
      } else {
        blink_timer = timer_read();
      }
    }
  }
  // serial
  uint8_t command = 0;
  if (blinked) {
    command = 1;
  }
  serial_master_buffer[SERIAL_MASTER_BUFFER_LENGTH - 1] = command;
}

void update_slave_status(void) {
  uint8_t command = serial_master_buffer[SERIAL_MASTER_BUFFER_LENGTH - 1];
  if (command) {
    blinked = true;
  } else {
    blinked = false;
  }
}

void render_master(struct CharacterMatrix *matrix) {
  char *data;
  if (blinked) {
    data = master_blinked;
  } else {
    data = master_normal;
  }
  matrix_write(matrix, data);
}

void render_slave(struct CharacterMatrix *matrix) {
  char *data;
  if (blinked) {
    data = slave_blinked;
  } else {
    data = slave_normal;
  }
  matrix_write(matrix, data);
}

void materia_task(struct CharacterMatrix *matrix, bool master) {
  if (master) {
    update_master_status();
    render_master(matrix);
  } else {
    update_slave_status();
    render_slave(matrix);
  }
}
